    function getLang() {
        return localStorage.getItem(STORAGE_KEY) === 'en' ? 'en' : 'hr';
    }

    function setLang(lang) {
        localStorage.setItem(STORAGE_KEY, lang === 'en' ? 'en' : 'hr');
        location.reload();
    }

    function t(key, params) {
        const lang = getLang();
        let s = (lang === 'en' ? EN : HR)[key] ?? HR[key] ?? key;
        if (params && typeof params === 'object') {
            Object.keys(params).forEach(k => {
                s = s.split(`{${k}}`).join(String(params[k]));
            });
        }
        return s;
    }

    function translateApiError(msg) {
        if (!msg || getLang() !== 'en') return msg;
        return apiErrorHRtoEN[msg] || msg;
    }

    function applyStaticI18n() {
        const lang = getLang();
        document.documentElement.lang = lang === 'en' ? 'en' : 'hr';
        document.title = t('app.title');

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (key) el.textContent = t(key);
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (key) el.placeholder = t(key);
        });
        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            const key = el.getAttribute('data-i18n-title');
            if (key) el.title = t(key);
        });
        document.querySelectorAll('[data-i18n-label]').forEach(el => {
            const key = el.getAttribute('data-i18n-label');
            if (key) el.textContent = t(key);
        });
        document.querySelectorAll('[data-i18n-aria]').forEach(el => {
            const key = el.getAttribute('data-i18n-aria');
            if (key) el.setAttribute('aria-label', t(key));
        });
        document.querySelectorAll('option[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (key) el.textContent = t(key);
        });

        document.querySelectorAll('.lang-select').forEach(sel => {
            sel.value = lang;
        });
    }

    global.I18N = { getLang, setLang, t, translateApiError, applyStaticI18n };
